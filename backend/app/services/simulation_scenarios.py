def get_scenario_by_id(scenario_id: str) -> Scenario | None:
    for s in SCENARIOS:
        if s.id == scenario_id:
            return s
    return None


def list_scenarios() -> list[dict]:
    return [s.to_dict() for s in SCENARIOS]
