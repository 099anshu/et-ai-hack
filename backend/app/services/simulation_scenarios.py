"""Pre-built scam call simulation scenarios.

Each scenario includes metadata, a full script (for transcript fallback),
and a reference to a pre-generated TTS audio file.  The audio files are
generated by ``scripts/generate_scenario_audio.py``.
"""
from __future__ import annotations

import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional

logger = logging.getLogger(__name__)

SCENARIOS_AUDIO_DIR = Path(__file__).resolve().parent.parent / "data" / "scenarios"


@dataclass
class ScenarioLine:
    speaker: str  # CALLER | VICTIM
    text: str
    time_offset: str  # display time e.g. "0:03"
    intent: str = "NORMAL"
    language: str = "hi"


@dataclass
class Scenario:
    id: str
    title: str
    description: str
    language: str
    duration_seconds: int
    expected_threat_score: float
    expected_intents: list[str] = field(default_factory=list)
    script: list[ScenarioLine] = field(default_factory=list)
    audio_filename: str | None = None

    @property
    def audio_path(self) -> Path | None:
        if self.audio_filename:
            p = SCENARIOS_AUDIO_DIR / self.audio_filename
            return p if p.exists() else None
        return None

    def to_dict(self) -> dict:
        return {
            "id": self.id,
            "title": self.title,
            "description": self.description,
            "language": self.language,
            "duration_seconds": self.duration_seconds,
            "expected_threat_score": self.expected_threat_score,
            "expected_intents": self.expected_intents,
            "has_audio": self.audio_path is not None,
            "script": [
                {
                    "speaker": l.speaker,
                    "text": l.text,
                    "time": l.time_offset,
                    "intent": l.intent,
                    "language": l.language,
                }
                for l in self.script
            ],
        }


